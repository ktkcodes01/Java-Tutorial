package Basics;
public class UnaryOperator{
    public static void main(String[] args) {
        int a= 10;
        int b= 20;
        boolean c = true;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("a++ = " + a++);
        System.out.println("++b = " + ++b);
        System.out.println("a-- = " + a--);
        System.out.println("--b = " + --b);
        System.out.println("!c = " + !c);
    }
}